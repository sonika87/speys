package com.speys.Test.Bean;

/**
 *
 * @author Julio
 */
public class ValorRespuestaBean {
    
    private int numero;
    private int a;
    private int b;
    private int c;
    
    public ValorRespuestaBean(int numero, int a, int b, int c) {
        this.numero = numero;
        this.a = a;
        this.b = b;
        this.c = c;
    }
    

    /**
     * @return the numero
     */
    public int getNumero() {
        return numero;
    }

    /**
     * @param numero the numero to set
     */
    public void setNumero(int numero) {
        this.numero = numero;
    }

    /**
     * @return the a
     */
    public int getA() {
        return a;
    }

    /**
     * @param a the a to set
     */
    public void setA(int a) {
        this.a = a;
    }

    /**
     * @return the b
     */
    public int getB() {
        return b;
    }

    /**
     * @param b the b to set
     */
    public void setB(int b) {
        this.b = b;
    }

    /**
     * @return the c
     */
    public int getC() {
        return c;
    }

    /**
     * @param c the c to set
     */
    public void setC(int c) {
        this.c = c;
    }
    
    
}
